        <div id="performance-criteria" class="p-6">
            <h2 class="text-xl p-3 underline font-bold">Performance Criteria</h2>
            <ol class="px-3 list-decimal">
                <h3 class="font-bold py-2">Theory</h3>
                <li class="py-1">A minimum of 3 written examinations will be conducted for each subject, each academic year.</li>
                <li class="py-1">Only the three best performances for each subjects' written examination, will contribute towards the final average internal marks.</li>
            </ol>
            <ol class="px-3 list-decimal">
                <h3 class="font-bold py-2">Practical</h3>
                <li class="py-1">At least one practical examination per subject, where it is required, will take place and contribute towards a seperate practical grade.</li>
                <li class="py-1">A completed practical record book must be submitted, for each subject, at least 15 days before theory examination for internal evaluation.</li>
            </ol>
        </div>
        
        <div class="p-6 bg-red-400/20 rounded">
            <ol class="px-3 list-disc">
                <li class="py-1">A minimum of 50% in each subject, in both written and practical examinations is required to sit University Examinations.</li>
            </ol>
        </div>
        
        
        <div id="attendance-criteria" class="p-6">
            <h2 class="text-xl p-3 underline font-bold">Attendance Criteria</h2>
            <ol class="px-3 list-decimal">
                <li class="py-1">Between August 1st and 31st July of the same academic year, attendance must be greater than 90% for each subject.</li>
                <li class="py-1">If only a minority of subjects do not meet the attendance criteria, a student may sit only the exams for the subjects he has achieved a greater than 90% attendance score.</li>
                <li class="py-1">If only a minority of subjects do not meet the attendance criteria, the student will be held back for only the subjects he has failed to meet the attendance criteria for.</li>
                <li class="py-1">If a student fails to achieve greater than 90% attendance for the majority of subjects, the student will be required to retake the year.</li>
            </ol>
        </div>
        
        <div class="p-6 bg-red-400/20 rounded">
            <ol class="px-3 list-disc">
                <li class="py-1">No concessions or exceptions will be made for leave of any kind during term time. Including pregnancy, child birth or medical conditions.</li>
                <li class="py-1">A temporary break from study is permissible for fair reasons (such as prolonged hospitalisation), but must be submitted before registering for semester examinations. The terms of readmission will be discussed as and when required.</li>
            </ol>
        </div>
        
        <div id="examination-schedule" class="p-6">
            <h2 class="text-xl p-3 underline font-bold">Examination Schedule</h2>
            <ol class="px-3 list-decimal">
                <li class="py-1">Internal examination timetables will be released at least 1 week in advance.</li>
                <li class="py-1">All internal examinations will take place before 10th July of the same academic Year.</li>
                <li class="py-1">A student will only be permitted into exam halls if they are registered before 30th September of the same academic year.</li>
            </ol>
        </div>
        
        <div class="p-6 bg-red-400/20 rounded">
            <ol class="px-3 list-disc">
                <li class="py-1">Make-up/ Arrears examinations must take place before 10th January of the next academic year.</li>
            </ol>
        </div>
        
        <div id="arrears-policy" class="p-6">
            <h2 class="text-xl p-3 underline font-bold">Arrears Policy</h2>
            <ol class="px-3 list-decimal">
                <li class="py-1">If a candidate fails in either theory or practical examinations, they must resit both theory and practical exams.</li>
                <li class="py-1">Only 4 re-attempts are allowed in each subject.</li>
                <li class="py-1">A candidate failing in more than 3 subjects (defined as a majority) will not be promoted to the next year. </li>
                <li class="py-1">Candidates are permitted to carry over any three failed subjects to the 2nd and 3rd year.</li>
                <li class="py-1">No arrear can be carried over to the final year.</li>
            </ol>
