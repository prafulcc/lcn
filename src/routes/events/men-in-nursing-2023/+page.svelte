            <h3 class="font-bold py-2">Submit abstracts to email: research@lcon.in</h3>
            <h3 class="font-bold py-2">For inclusion into the conference programme, the commitee will only consider abstracts which meet the following criteria:</h3>
            <ol class="px-3 list-decimal">
                <li class="py-1">The abstract must be about a research project related to the conference theme.</li>
                <li class="py-1">Material presented must be made relevant to an international audience (e.g. a local study must be placed within some generalised international context).</li>
                <li class="py-1">There must be a focus on analysis and insights, but abstracts of empirical studies must also outline the research process.</li>
                <li class="py-1">Presenters must indicate clearly the month(s) and/ or year(s) when the data was collected at a minimum. (n.b. an abstract will not be accepted if the data has not yet been collected).</li>
                <li class="py-1">Abstracts reporting on the results of research students should follow the general structure of: background, aims, methods, results, discussion and conclusion.</li>
                <li class="py-1">Relevant contextual information, including definitions of specialist terms must be given.</li>
                <li class="py-1">Authors must consider and specify how their paper contributes towards the themes of conference.</li>
                <li class="py-1">All abstracts must be text-based and written in english (i.e no graphs and diagrams).</li>
            </ol>
